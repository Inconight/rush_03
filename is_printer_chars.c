int		is_printer_chars(char *cadena)
{
	int i;

	i = 0;
	while (cadena[i] != '\0')
	{
		if (cadena[i] <= 32 || cadena[i] >= 126)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
